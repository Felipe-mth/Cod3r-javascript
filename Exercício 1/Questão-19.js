/*18) Faça um programa que leia um número entre 0 e 10, e escreva este número por extenso. Use o comando
switch. Crie um case default que escreva ‘Número fora do intervalo.’*/

function intervalo(a){
    switch(a){
        case a = 1:
            console.log('um')
            break;
        case a = 2:
            console.log('dois')
            break;
        case a = 3:
            console.log('três')
            break;
        case a = 4:
            console.log('quatro')
            break;
        case a = 5:
            console.log('cinco')
            break;
        case a = 6:
            console.log('seis')
            break;
        case a = 7:
            console.log('sete')
            break;
        case a = 8:
            console.log('oito')
            break;
        case a = 9:
            console.log('nove')
            break;
        case a = 10:
            console.log('dez')
            break;
        default:
            console.log('fora do intervalo')
    }
}
intervalo()